    /**
     * Método que regresa el archivo selecionado en tipo File
     *
     * @return String
     */
    public File seleccionarArchivo() {
        JFileChooser fileChooser = new JFileChooser(".");
        FileFilter filtro = new FileNameExtensionFilter("Archivos xlsx (.xlsx)", "xlsx");
        fileChooser.setFileFilter(filtro);
        int valor = fileChooser.showOpenDialog(fileChooser);
        if (valor == JFileChooser.APPROVE_OPTION) {
            String ruta = fileChooser.getSelectedFile().getAbsolutePath();
            File f = new File(ruta);
            System.out.println("Se ha selecionado el archivo");
            return f;
        } else {
            System.out.println("No se ha selecionado el archivo");
            return null;
        }
    }

