    private void btn_catalogoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_catalogoMousePressed
        jpui.setCatalogo(null);
        File archivo = seleccionarArchivo();
        if (archivo != null) {
            jpui.getCuentasList().clear();
            jpui.setCatalogo(jpui.trasformaWB(archivo));
            jpui.leerCatalogo();
            if (!jpui.getCuentasList().isEmpty()) {
                catalogo_texto.setText("Catálogo agregado");
                catalogo_texto.setForeground(Color.GREEN);
                jpui.leerCuentas();
            } else {
                catalogo_texto.setText("Error, selecciona nuevamente");
                catalogo_texto.setForeground(Color.RED);
            }
            verificarArchivos();
        } else {
            catalogo_texto.setText("No se ha seleccionado ningún archivo");
            catalogo_texto.setForeground(Color.LIGHT_GRAY);
        }
    }//GEN-LAST:event_btn_catalogoMousePressed

    private void conciliaciónMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_conciliaciónMousePressed
        jpui.setConciliacion(null);
        File archivo = seleccionarArchivo();
        if (archivo != null) {
            jpui.setConciliacion(jpui.trasformaWB(archivo));
            conciliacion_texto.setText("Conciliación agregada");
            conciliacion_texto.setForeground(Color.GREEN);
            verificarArchivos();
        } else {
            conciliacion_texto.setText("No se ha seleccionado ningún archivo");
            conciliacion_texto.setForeground(Color.LIGHT_GRAY);
        }
    }//GEN-LAST:event_conciliaciónMousePressed

